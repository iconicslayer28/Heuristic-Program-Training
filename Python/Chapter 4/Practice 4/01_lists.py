# Create a list using [] 
a  = [ 1,2,3,4,556,10 ] 
# Print the list using () function 
print(a) 


# Access using index using a[0], a[1], a[2] 

print(a[3]) 

# Change the value of list using 
 
a[3] = 23
print(a)  

# Inserting the value

a.insert(6,12)
print(a)

#  We can create a list with items of  different types 

b = [ 'Aniket', '34', "False" ] 
